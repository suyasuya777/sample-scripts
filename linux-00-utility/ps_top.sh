#!/bin/bash
# ============================================================
# ps / top / htop サンプル集 - SRE向け
# ============================================================

# ============================================================
# 1. ps：全プロセスを標準フォーマットで表示
# ============================================================
echo "=== 1. 全プロセス一覧 ==="
ps aux | head -10

# ============================================================
# 2. ps：特定プロセスを名前で検索
# ============================================================
echo ""
echo "=== 2. sshd プロセスを検索 ==="
ps aux | grep sshd | grep -v grep

# ============================================================
# 3. ps：CPU使用率上位5プロセス
# ============================================================
echo ""
echo "=== 3. CPU使用率上位5プロセス ==="
ps aux --sort=-%cpu | head -6

# ============================================================
# 4. ps：メモリ使用率上位5プロセス
# ============================================================
echo ""
echo "=== 4. メモリ使用率上位5プロセス ==="
ps aux --sort=-%mem | head -6

# ============================================================
# 5. ps：特定ユーザーのプロセス一覧
# ============================================================
echo ""
echo "=== 5. root ユーザーのプロセス一覧 ==="
ps -u root | head -10

# ============================================================
# 6. ps：プロセスツリーを表示（親子関係）
# ============================================================
echo ""
echo "=== 6. プロセスツリー ==="
ps axjf | head -20

# ============================================================
# 7. ps：特定PIDの詳細情報
# ============================================================
echo ""
echo "=== 7. PID 1 の詳細情報 ==="
ps -p 1 -o pid,ppid,user,stat,vsz,rss,comm

# ============================================================
# 8. ps：カスタムフォーマットで表示
# ============================================================
echo ""
echo "=== 8. PID・プロセス名・CPU・メモリ・起動時刻をカスタム表示 ==="
ps -eo pid,comm,%cpu,%mem,lstart --sort=-%cpu | head -10

# ============================================================
# 9. ps：ゾンビプロセスの確認
# ============================================================
echo ""
echo "=== 9. ゾンビプロセスの確認 ==="
ps aux | awk '$8 == "Z" {print "ゾンビ検出:", $0}'
echo "(ゾンビなし)"

# ============================================================
# 10. ps：プロセス数をユーザー別に集計
# ============================================================
echo ""
echo "=== 10. ユーザー別プロセス数 ==="
ps aux | awk 'NR>1 {count[$1]++} END {for (u in count) print count[u], u}' | sort -rn | head -10

# ============================================================
# 11. top：バッチモードで1回だけ出力（-b -n 1）
# ============================================================
echo ""
echo "=== 11. top をバッチモードで実行（ヘッダー部分のみ） ==="
top -b -n 1 | head -15

# ============================================================
# 12. top：特定プロセスのみ監視
# ============================================================
echo ""
echo "=== 12. top で PID 1 のみ表示（バッチモード） ==="
top -b -n 1 -p 1 | tail -4

# ============================================================
# 13. htop：非対話モードで出力（インストール済みの場合）
# ============================================================
echo ""
echo "=== 13. htop の主な使い方（インタラクティブTUIツール） ==="
cat << 'HELP'
起動方法:
  htop                      # 通常起動
  htop -u appuser           # 特定ユーザーのプロセスのみ表示
  htop -p 1234,5678         # 特定PIDのみ表示
  htop -s PERCENT_CPU       # CPU使用率でソートして起動

主なキー操作:
  F2 / S  : 設定画面
  F3 / /  : プロセス名で検索
  F4 / \  : フィルタ
  F5      : ツリー表示切り替え
  F6      : ソートキー選択
  F9 / k  : シグナル送信（プロセスKill）
  F10 / q : 終了
  u       : ユーザーでフィルタ
  t       : ツリービュー切り替え
HELP

# ============================================================
# 14. 実用例：高CPU プロセスを自動検出してアラート
# ============================================================
echo ""
echo "=== 14. CPU 50% 超のプロセスを検出 ==="
THRESHOLD=50
ps aux --sort=-%cpu | awk -v threshold="$THRESHOLD" '
  NR > 1 && $3 > threshold {
    printf "⚠️  高CPU検出: PID=%s USER=%s CPU=%.1f%% CMD=%s\n", $2, $1, $3, $11
    found=1
  }
  END { if (!found) print "✅ 高CPUプロセスなし（閾値: " threshold "%）" }
'

# ============================================================
# 15. 実用例：メモリ使用量をMB単位で整形表示
# ============================================================
echo ""
echo "=== 15. メモリ使用量上位5プロセス（MB表示） ==="
ps aux --sort=-%mem | awk '
  NR == 1 { print "USER\t\tPID\tMEM%\tRSS(MB)\tCOMMAND" }
  NR > 1 && NR <= 6 {
    printf "%-12s\t%s\t%.1f\t%.1f\t%s\n", $1, $2, $4, $6/1024, $11
  }
'

#!/bin/bash
# ============================================================
# tcpdump サンプル集 - SRE向け
# ============================================================
# ※ tcpdump は root 権限または CAP_NET_RAW 権限が必要です
# ※ 本番環境では通信量に注意して使用してください

# ============================================================
# 1. 基本：インターフェースを指定してキャプチャ（-i）
# ============================================================
echo "=== 1. eth0 のパケットを10件キャプチャ ==="
tcpdump -i eth0 -c 10 2>/dev/null || \
tcpdump -i any -c 10 2>/dev/null || \
echo "(権限不足または利用可能なインターフェースなし)"

# ============================================================
# 2. 利用可能なインターフェースを確認
# ============================================================
echo ""
echo "=== 2. 利用可能なインターフェースを一覧表示 ==="
tcpdump -D 2>/dev/null || ip link show | grep "^[0-9]"

# ============================================================
# 3. 特定ホストとの通信をキャプチャ（host）
# ============================================================
echo ""
echo "=== 3. 特定ホストとの通信（コマンド例） ==="
cat << 'HELP'
# 特定ホストとの通信をキャプチャ
tcpdump -i eth0 host 192.168.1.100

# 特定ホストへの送信のみ
tcpdump -i eth0 dst host 192.168.1.100

# 特定ホストからの受信のみ
tcpdump -i eth0 src host 192.168.1.100
HELP

# ============================================================
# 4. 特定ポートの通信をキャプチャ（port）
# ============================================================
echo ""
echo "=== 4. 特定ポートのキャプチャ（コマンド例） ==="
cat << 'HELP'
# ポート80（HTTP）の通信
tcpdump -i eth0 port 80

# ポート443（HTTPS）の通信
tcpdump -i eth0 port 443

# ポート範囲（8000〜8080）
tcpdump -i eth0 portrange 8000-8080

# 複数ポート
tcpdump -i eth0 port 80 or port 443
HELP

# ============================================================
# 5. プロトコルでフィルタ
# ============================================================
echo ""
echo "=== 5. プロトコルフィルタ（コマンド例） ==="
cat << 'HELP'
# TCPのみ
tcpdump -i eth0 tcp

# UDPのみ
tcpdump -i eth0 udp

# ICMPのみ（pingの確認）
tcpdump -i eth0 icmp

# DNS通信（UDP 53）
tcpdump -i eth0 udp port 53
HELP

# ============================================================
# 6. ファイルに保存（-w）と読み込み（-r）
# ============================================================
echo ""
echo "=== 6. ファイルへの保存と読み込み（コマンド例） ==="
cat << 'HELP'
# pcapファイルに保存（30秒間）
tcpdump -i eth0 -w /tmp/capture.pcap -G 30 -W 1

# 保存したファイルを読み込んで解析
tcpdump -r /tmp/capture.pcap

# Wiresharkで開くことも可能
wireshark /tmp/capture.pcap
HELP

# ============================================================
# 7. パケットの内容を表示（-A / -X）
# ============================================================
echo ""
echo "=== 7. パケット内容の表示オプション ==="
cat << 'HELP'
# ASCIIで表示（HTTPの内容確認など）
tcpdump -i eth0 -A port 80

# HEXとASCIIで表示
tcpdump -i eth0 -X port 80

# ヘッダーのみ詳細表示
tcpdump -i eth0 -v port 80

# さらに詳細
tcpdump -i eth0 -vv port 80
HELP

# ============================================================
# 8. タイムスタンプのフォーマット変更
# ============================================================
echo ""
echo "=== 8. タイムスタンプ形式の変更 ==="
cat << 'HELP'
# 絶対時刻で表示（デフォルト）
tcpdump -i eth0 -tttt -c 5

# Unix タイムスタンプで表示
tcpdump -i eth0 -ttt -c 5
HELP

# ============================================================
# 9. 複合フィルタ（and / or / not）
# ============================================================
echo ""
echo "=== 9. 複合フィルタの例 ==="
cat << 'HELP'
# 特定ホストのポート80通信
tcpdump -i eth0 host 10.0.0.1 and port 80

# ポート22以外の通信
tcpdump -i eth0 not port 22

# 特定サブネットとの通信
tcpdump -i eth0 net 192.168.1.0/24

# 特定ホストへのHTTP/HTTPS通信
tcpdump -i eth0 host 10.0.0.1 and (port 80 or port 443)
HELP

# ============================================================
# 10. キャプチャサイズの制限（-s スナップショット長）
# ============================================================
echo ""
echo "=== 10. キャプチャサイズの制限 ==="
cat << 'HELP'
# 全パケットをキャプチャ（デフォルト262144バイト）
tcpdump -i eth0 -s 0

# ヘッダーのみ（96バイト）で高速キャプチャ
tcpdump -i eth0 -s 96

# 件数を制限して自動終了（-c）
tcpdump -i eth0 -c 100 -w /tmp/100packets.pcap
HELP

# ============================================================
# 11. HTTPリクエストの内容を確認
# ============================================================
echo ""
echo "=== 11. HTTPリクエスト/レスポンスの確認（コマンド例） ==="
cat << 'HELP'
# GETリクエストのURLを確認
tcpdump -i eth0 -A port 80 | grep "GET\|POST\|Host:"

# レスポンスステータスを確認
tcpdump -i eth0 -A port 80 | grep "HTTP/1\."
HELP

# ============================================================
# 12. SYN パケットのみキャプチャ（接続試行の確認）
# ============================================================
echo ""
echo "=== 12. SYNパケット（接続試行）のみキャプチャ ==="
cat << 'HELP'
# TCPフラグでSYNのみフィルタ（SYN-ACKは除外）
tcpdump -i eth0 "tcp[tcpflags] & (tcp-syn) != 0 and tcp[tcpflags] & (tcp-ack) == 0"

# SYN-ACK のみ（接続受け入れ確認）
tcpdump -i eth0 "tcp[tcpflags] & (tcp-syn|tcp-ack) == (tcp-syn|tcp-ack)"

# RSTパケット（接続拒否・異常切断）を確認
tcpdump -i eth0 "tcp[tcpflags] & tcp-rst != 0"
HELP

# ============================================================
# 13. 実用例：障害調査フロー
# ============================================================
echo ""
echo "=== 13. 障害調査フロー ==="
cat << 'HELP'
# Step1: まず対象ホスト・ポートの通信を広くキャプチャ
tcpdump -i eth0 host 10.0.0.1 and port 8080 -w /tmp/debug.pcap -c 1000

# Step2: キャプチャ結果を確認（タイムスタンプ付き）
tcpdump -r /tmp/debug.pcap -tttt | head -50

# Step3: エラーパケット（RST）を確認
tcpdump -r /tmp/debug.pcap "tcp[tcpflags] & tcp-rst != 0"

# Step4: 再送パケットを確認（遅延・パケロスの兆候）
tcpdump -r /tmp/debug.pcap "tcp[tcpflags] & tcp-syn != 0" | wc -l
HELP

# ============================================================
# 14. 実用例：DNS問い合わせの確認
# ============================================================
echo ""
echo "=== 14. DNS問い合わせのリアルタイム確認 ==="
cat << 'HELP'
# DNS通信を確認（UDP/TCP ポート53）
tcpdump -i eth0 port 53 -n

# DNS問い合わせのホスト名を抽出
tcpdump -i eth0 port 53 -A 2>/dev/null | grep -oP "[a-zA-Z0-9.-]+\.[a-zA-Z]{2,}" | sort | uniq -c | sort -rn
HELP

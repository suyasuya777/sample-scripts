#!/bin/bash
# ============================================================
# ss サンプル集 - SRE向け
# ============================================================

# ============================================================
# 1. ss：全ソケットの一覧
# ============================================================
echo "=== 1. ss：全ソケット一覧 ==="
ss | head -15

# ============================================================
# 2. ss：LISTEN中のTCPポート一覧
# ============================================================
echo ""
echo "=== 2. LISTEN中のTCPポート ==="
ss -tlnp

echo ""
echo "【オプションの意味】"
echo "  -t: TCP  -l: LISTEN  -n: 数値表示  -p: プロセス名表示"

# ============================================================
# 3. ss：LISTEN中のUDPポート一覧
# ============================================================
echo ""
echo "=== 3. LISTEN中のUDPポート ==="
ss -ulnp

# ============================================================
# 4. ss：ESTABLISHED状態のTCP接続
# ============================================================
echo ""
echo "=== 4. ESTABLISHED接続一覧 ==="
ss -tn state established | head -15

# ============================================================
# 5. ss：特定ポートの接続確認
# ============================================================
echo ""
echo "=== 5. ポート 22 の接続状態 ==="
ss -tnp | grep ":22"

# ============================================================
# 6. ss：接続状態の集計
# ============================================================
echo ""
echo "=== 6. TCP接続状態の集計 ==="
ss -tan | awk 'NR>1 {count[$1]++} END {for (s in count) print count[s], s}' | sort -rn

# ============================================================
# 7. ss：特定プロセスのソケット確認
# ============================================================
echo ""
echo "=== 7. sshd のソケット確認 ==="
ss -tp | grep sshd | head -5

# ============================================================
# 8. ss：送受信バッファサイズを表示（-m）
# ============================================================
echo ""
echo "=== 8. 送受信バッファサイズ付きで表示 ==="
ss -tlnpm | head -10

# ============================================================
# 9. ss：Unix ドメインソケット一覧
# ============================================================
echo ""
echo "=== 9. Unixドメインソケット一覧 ==="
ss -xlnp | head -10

# ============================================================
# 10. ss：TIME_WAIT状態の接続確認
# ============================================================
echo ""
echo "=== 10. タイムウェイト状態の接続確認 ==="
ss -tan state time-wait | head -10
echo "TIME_WAIT接続数: $(ss -tan state time-wait | grep -c TIME-WAIT || echo 0)"

# ============================================================
# 11. 実用例：特定ポートが開いているか確認
# ============================================================
echo ""
echo "=== 11. ポートの開放確認 ==="
check_port() {
    local port=$1
    if ss -tlnp | grep -q ":${port} "; then
        echo "✅ ポート $port は LISTEN中"
        ss -tlnp | grep ":${port} "
    else
        echo "⚠️  ポート $port は未使用"
    fi
}
check_port 22
check_port 80
check_port 8080

# ============================================================
# 12. 実用例：ESTABLISHED接続数が閾値を超えたら警告
# ============================================================
echo ""
echo "=== 12. ESTABLISHED接続数の監視 ==="
THRESHOLD=100
COUNT=$(ss -tan state established | grep -c ESTAB || echo 0)
echo "現在の ESTABLISHED 接続数: $COUNT"
if [ "$COUNT" -gt "$THRESHOLD" ]; then
    echo "⚠️  接続数が閾値 $THRESHOLD を超えています"
else
    echo "✅ 接続数は正常範囲内"
fi

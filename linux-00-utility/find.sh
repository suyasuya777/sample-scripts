#!/bin/bash
# ============================================================
# find サンプル集 - SRE向け
# ============================================================

# ============================================================
# 1. 基本：ファイル名で検索（-name）
# ============================================================
echo "=== 1. ファイル名で検索 ==="
find /tmp -name "*.log" 2>/dev/null | head -10

echo ""
echo "=== 1b. 大文字小文字を無視して検索（-iname） ==="
find /tmp -iname "*.LOG" 2>/dev/null | head -5

# ============================================================
# 2. ファイルタイプで絞り込み（-type）
# ============================================================
echo ""
echo "=== 2. ファイルのみ検索（-type f） ==="
find /tmp -type f 2>/dev/null | head -10

echo ""
echo "=== 2b. ディレクトリのみ検索（-type d） ==="
find /tmp -type d 2>/dev/null | head -5

echo ""
echo "=== 2c. シンボリックリンクのみ（-type l） ==="
find /etc -type l 2>/dev/null | head -5

# ============================================================
# 3. 更新時刻で検索（-mtime / -mmin）
# ============================================================
echo ""
echo "=== 3. 24時間以内に更新されたファイル ==="
find /tmp -type f -mtime -1 2>/dev/null | head -10

echo ""
echo "=== 3b. 7日以上前に更新されたファイル ==="
find /tmp -type f -mtime +7 2>/dev/null | head -5

echo ""
echo "=== 3c. 30分以内に更新されたファイル（-mmin） ==="
find /tmp -type f -mmin -30 2>/dev/null | head -5

# ============================================================
# 4. ファイルサイズで検索（-size）
# ============================================================
echo ""
echo "=== 4. 100MB以上のファイルを検索 ==="
find / -type f -size +100M 2>/dev/null | head -10

echo ""
echo "=== 4b. 空ファイル（0バイト）を検索 ==="
find /tmp -type f -empty 2>/dev/null | head -5

echo ""
echo "=== 4c. 1KB以上100KB未満のファイル ==="
find /tmp -type f -size +1k -size -100k 2>/dev/null | head -5

# ============================================================
# 5. パーミッションで検索（-perm）
# ============================================================
echo ""
echo "=== 5. 777パーミッションのファイルを検索 ==="
find /tmp -type f -perm 777 2>/dev/null | head -5

echo ""
echo "=== 5b. 実行権限を持つファイル ==="
find /usr/local/bin -type f -perm /111 2>/dev/null | head -10

echo ""
echo "=== 5c. SUIDビットが立っているファイル（セキュリティ確認） ==="
find / -type f -perm /4000 2>/dev/null | head -10

# ============================================================
# 6. 所有者・グループで検索（-user / -group）
# ============================================================
echo ""
echo "=== 6. root所有のファイルを検索 ==="
find /tmp -type f -user root 2>/dev/null | head -5

echo ""
echo "=== 6b. 特定グループのファイル ==="
find /var/log -type f -group adm 2>/dev/null | head -5

# ============================================================
# 7. 検索結果に対してコマンドを実行（-exec）
# ============================================================
echo ""
echo "=== 7. 検索したファイルの詳細を表示（-exec ls -lh） ==="
find /tmp -type f -name "*.log" -exec ls -lh {} \; 2>/dev/null | head -10

echo ""
echo "=== 7b. 複数ファイルをまとめて処理（-exec ... +） ==="
find /tmp -type f -name "*.json" -exec ls -lh {} + 2>/dev/null | head -5

# ============================================================
# 8. xargsと組み合わせ（大量ファイルの効率処理）
# ============================================================
echo ""
echo "=== 8. xargs と組み合わせてgrep ==="
find /tmp -type f -name "*.log" 2>/dev/null | xargs grep -l "ERROR" 2>/dev/null | head -5

# ============================================================
# 9. 検索深さを制限（-maxdepth / -mindepth）
# ============================================================
echo ""
echo "=== 9. 検索深さを2階層に制限 ==="
find /etc -maxdepth 2 -type f -name "*.conf" 2>/dev/null | head -10

echo ""
echo "=== 9b. 2階層以下のみ検索（-mindepth） ==="
find /etc -mindepth 2 -maxdepth 3 -type f 2>/dev/null | head -5

# ============================================================
# 10. 複数条件の組み合わせ（-and / -or / -not）
# ============================================================
echo ""
echo "=== 10. 複数条件の組み合わせ ==="
# .log または .txt ファイル
find /tmp -type f \( -name "*.log" -o -name "*.txt" \) 2>/dev/null | head -5

# .shファイルかつ7日以内に更新
find /home -type f -name "*.sh" -mtime -7 2>/dev/null | head -5

# ============================================================
# 11. 実用例：古いログファイルを削除
# ============================================================
echo ""
echo "=== 11. 30日以上前のログファイルを確認（削除前の確認） ==="
find /tmp -type f -name "*.log" -mtime +30 2>/dev/null | head -10
# 実際の削除は以下（確認後に実行）
# find /tmp -type f -name "*.log" -mtime +30 -delete

# ============================================================
# 12. 実用例：大容量ファイルを特定してディスク逼迫を調査
# ============================================================
echo ""
echo "=== 12. 100MB超のファイルをサイズ順に表示 ==="
find / -type f -size +100M 2>/dev/null \
  | xargs ls -lh 2>/dev/null \
  | sort -k5 -rh \
  | head -10

# ============================================================
# 13. 実用例：直近1時間に更新された設定ファイルを確認
# ============================================================
echo ""
echo "=== 13. 直近1時間に更新された設定ファイル ==="
find /etc -type f -mmin -60 2>/dev/null | head -10

# ============================================================
# 14. 実用例：パーミッションが不正なファイルを検出
# ============================================================
echo ""
echo "=== 14. world-writable なファイルを検出（セキュリティ確認） ==="
find /etc -type f -perm -o+w 2>/dev/null | head -10
echo "(world-writable なファイルが存在する場合はセキュリティリスクの可能性あり)"

#!/bin/bash
# ============================================================
# sed サンプル集 - SRE向け
# ============================================================

# ── 前提：サンプルファイルを作成 ─────────────────────────
cat > /tmp/config.conf << 'EOF'
# アプリケーション設定
DB_HOST=db-prod-01
DB_PORT=5432
DB_NAME=appdb
DB_USER=appuser
DB_PASS=secret123

APP_ENV=production
APP_PORT=8080
LOG_LEVEL=INFO
EOF

cat > /tmp/app.log << 'EOF'
2024-01-15 10:00:01 INFO  [web] Request GET /api/users 200 12ms
2024-01-15 10:00:02 ERROR [db]  Connection timeout: host=db01 port=5432
2024-01-15 10:00:03 WARN  [web] Slow response GET /api/items 1523ms
2024-01-15 10:00:04 INFO  [web] Request POST /api/users 201 45ms
2024-01-15 10:00:05 ERROR [web] 500 Internal Server Error /api/orders
EOF

echo "=== 元ファイル ==="
cat /tmp/config.conf
echo ""

# ============================================================
# 1. 文字列の置換（s/置換前/置換後/）
# ============================================================
echo "=== 1. production を staging に置換 ==="
sed 's/production/staging/' /tmp/config.conf

# ============================================================
# 2. 全マッチを置換（g フラグ）
# ============================================================
echo ""
echo "=== 2. ログ内の INFO を [INFO] に置換（全件） ==="
sed 's/INFO/[INFO]/g' /tmp/app.log

# ============================================================
# 3. 特定行の置換（行番号指定）
# ============================================================
echo ""
echo "=== 3. 2行目のみ置換 ==="
sed '2s/db-prod-01/db-stg-01/' /tmp/config.conf

# ============================================================
# 4. 特定パターンを含む行を置換
# ============================================================
echo ""
echo "=== 4. DB_PASS の値をマスク ==="
sed 's/DB_PASS=.*/DB_PASS=****/' /tmp/config.conf

# ============================================================
# 5. 行の削除（d）
# ============================================================
echo ""
echo "=== 5. コメント行（# で始まる行）を削除 ==="
sed '/^#/d' /tmp/config.conf

echo ""
echo "=== 5b. 空行を削除 ==="
sed '/^$/d' /tmp/config.conf

# ============================================================
# 6. 特定行の削除（行番号）
# ============================================================
echo ""
echo "=== 6. 1行目を削除 ==="
sed '1d' /tmp/config.conf

echo ""
echo "=== 6b. 最終行を削除 ==="
sed '$d' /tmp/config.conf

# ============================================================
# 7. 行の表示（p）- 特定行だけ表示（-n と組み合わせ）
# ============================================================
echo ""
echo "=== 7. DB_ で始まる行だけ表示 ==="
sed -n '/^DB_/p' /tmp/config.conf

# ============================================================
# 8. 行の挿入・追加（i / a）
# ============================================================
echo ""
echo "=== 8. APP_ENV の前に行を挿入 ==="
sed '/^APP_ENV/i # ---- アプリ設定 ----' /tmp/config.conf

echo ""
echo "=== 8b. LOG_LEVEL の後に行を追加 ==="
sed '/^LOG_LEVEL/a LOG_FILE=/var/log/app.log' /tmp/config.conf

# ============================================================
# 9. ファイルを直接編集（-i）
# ============================================================
echo ""
echo "=== 9. ファイルを直接編集（バックアップ付き） ==="
cp /tmp/config.conf /tmp/config_edit.conf
sed -i.bak 's/DB_HOST=db-prod-01/DB_HOST=db-stg-01/' /tmp/config_edit.conf
echo "編集後:"
grep "DB_HOST" /tmp/config_edit.conf
echo "バックアップ:"
grep "DB_HOST" /tmp/config_edit.conf.bak

# ============================================================
# 10. 複数の操作を一度に実行（-e）
# ============================================================
echo ""
echo "=== 10. 複数操作を同時実行 ==="
sed -e 's/production/staging/' \
    -e 's/DB_HOST=db-prod-01/DB_HOST=db-stg-01/' \
    -e 's/APP_PORT=8080/APP_PORT=8081/' \
    /tmp/config.conf

# ============================================================
# 11. 正規表現グループの利用（\1 後方参照）
# ============================================================
echo ""
echo "=== 11. KEY=VALUE を KEY: VALUE 形式に変換 ==="
sed 's/^\([A-Z_]*\)=\(.*\)/\1: \2/' /tmp/config.conf | grep -v "^#" | grep -v "^$"

# ============================================================
# 12. 実用例：ログのタイムスタンプ部分を除去
# ============================================================
echo ""
echo "=== 12. ログからタイムスタンプを除去 ==="
sed 's/^[0-9-]* [0-9:]* //' /tmp/app.log

# ============================================================
# 13. 実用例：設定ファイルの環境切り替えスクリプト
# ============================================================
echo ""
echo "=== 13. prod → stg 環境切り替え（パイプライン）==="
cat /tmp/config.conf \
  | sed 's/production/staging/' \
  | sed 's/db-prod/db-stg/' \
  | sed 's/DB_PASS=.*/DB_PASS=stg_password/'

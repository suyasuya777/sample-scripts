#!/bin/bash
# ============================================================
# grep サンプル集 - SRE向け
# ============================================================

# ── 前提：サンプルログファイルを作成 ──────────────────────
cat > /tmp/app.log << 'EOF'
2024-01-15 10:00:01 INFO  [web] Request GET /api/users 200 12ms
2024-01-15 10:00:02 ERROR [db]  Connection timeout: host=db01 port=5432
2024-01-15 10:00:03 WARN  [web] Slow response GET /api/items 1523ms
2024-01-15 10:00:04 INFO  [web] Request POST /api/users 201 45ms
2024-01-15 10:00:05 ERROR [web] 500 Internal Server Error /api/orders
2024-01-15 10:00:06 INFO  [cache] Cache hit: user:1001
2024-01-15 10:00:07 ERROR [db]  Deadlock detected on table=orders
2024-01-15 10:00:08 INFO  [web] Request GET /api/users 200 8ms
2024-01-15 10:00:09 WARN  [cache] Cache miss rate high: 85%
2024-01-15 10:00:10 INFO  [web] Request DELETE /api/users/5 204 20ms
EOF

echo "=== サンプルログ ==="
cat /tmp/app.log
echo ""

# ============================================================
# 1. 基本検索：ERRORを含む行を抽出
# ============================================================
echo "=== 1. ERRORを含む行 ==="
grep "ERROR" /tmp/app.log

# ============================================================
# 2. 大文字小文字を無視して検索（-i）
# ============================================================
echo ""
echo "=== 2. 大文字小文字を無視して error を検索 ==="
grep -i "error" /tmp/app.log

# ============================================================
# 3. 行番号を表示（-n）
# ============================================================
echo ""
echo "=== 3. WARN を含む行を行番号付きで表示 ==="
grep -n "WARN" /tmp/app.log

# ============================================================
# 4. マッチしない行を表示（-v）: ERRORとWARN以外
# ============================================================
echo ""
echo "=== 4. ERRORでもWARNでもない行（INFO のみ） ==="
grep -v -E "ERROR|WARN" /tmp/app.log

# ============================================================
# 5. 正規表現（-E）: 5xxエラーのステータスコードを含む行
# ============================================================
echo ""
echo "=== 5. HTTPステータス 5xx を含む行 ==="
grep -E " 5[0-9]{2} " /tmp/app.log

# ============================================================
# 6. マッチした件数を表示（-c）
# ============================================================
echo ""
echo "=== 6. ERRORの件数 ==="
grep -c "ERROR" /tmp/app.log

# ============================================================
# 7. マッチ前後の行を表示（-A / -B / -C）
# ============================================================
echo ""
echo "=== 7. Deadlock の前後1行を表示 ==="
grep -C 1 "Deadlock" /tmp/app.log

# ============================================================
# 8. 複数ファイルを横断して検索（ファイル名も表示）
# ============================================================
echo ""
echo "=== 8. 複数ファイルを横断して ERROR を検索 ==="
cp /tmp/app.log /tmp/app2.log
grep -l "ERROR" /tmp/app.log /tmp/app2.log    # マッチしたファイル名のみ
grep -H "ERROR" /tmp/app.log /tmp/app2.log | head -4  # ファイル名:行

# ============================================================
# 9. 再帰検索（-r）: ディレクトリ配下を検索
# ============================================================
echo ""
echo "=== 9. /tmp 配下の .log ファイルで ERROR を再帰検索 ==="
grep -r "ERROR" /tmp/*.log

# ============================================================
# 10. マッチ部分のみ表示（-o）: IPアドレスを抽出
# ============================================================
echo ""
echo "=== 10. host= の値を抽出 ==="
grep -oE "host=[^ ]+" /tmp/app.log

# ============================================================
# 11. 実用例：直近1時間のERRORを数える（パイプライン）
# ============================================================
echo ""
echo "=== 11. [db] コンポーネントのERRORだけ抽出 ==="
grep "ERROR" /tmp/app.log | grep "\[db\]"

# ============================================================
# 12. 実用例：Slow response (1000ms超) の行を抽出
# ============================================================
echo ""
echo "=== 12. Slow response 行を抽出 ==="
grep "Slow" /tmp/app.log

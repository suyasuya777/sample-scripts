#!/bin/bash
# ============================================================
# lsof サンプル集 - SRE向け
# ============================================================

# ============================================================
# 1. 全オープンファイルを表示（件数確認）
# ============================================================
echo "=== 1. オープンファイルの総数 ==="
lsof | wc -l

# ============================================================
# 2. 特定プロセス（PID）が開いているファイルを表示
# ============================================================
echo ""
echo "=== 2. PID 1 が開いているファイル ==="
lsof -p 1 | head -15

# ============================================================
# 3. 特定ユーザーが開いているファイルを表示
# ============================================================
echo ""
echo "=== 3. root ユーザーが開いているファイル（上位10件） ==="
lsof -u root | head -10

# ============================================================
# 4. 特定ファイルを開いているプロセスを確認
# ============================================================
echo ""
echo "=== 4. /etc/passwd を開いているプロセス ==="
lsof /etc/passwd

# ============================================================
# 5. 特定ディレクトリ配下を開いているプロセス
# ============================================================
echo ""
echo "=== 5. /tmp 配下のファイルを開いているプロセス ==="
lsof +D /tmp | head -10

# ============================================================
# 6. ネットワーク接続の一覧（-i）
# ============================================================
echo ""
echo "=== 6. 全ネットワーク接続 ==="
lsof -i | head -15

# ============================================================
# 7. 特定ポートを使用しているプロセス
# ============================================================
echo ""
echo "=== 7. ポート 22 を使用しているプロセス ==="
lsof -i :22

echo ""
echo "=== 7b. ポート 80 または 443 を使用しているプロセス ==="
lsof -i :80 -i :443

# ============================================================
# 8. LISTEN 状態のポート一覧
# ============================================================
echo ""
echo "=== 8. LISTEN 中のポート一覧 ==="
lsof -i -sTCP:LISTEN

# ============================================================
# 9. ESTABLISHED 状態の接続一覧
# ============================================================
echo ""
echo "=== 9. ESTABLISHED 状態の接続一覧 ==="
lsof -i -sTCP:ESTABLISHED | head -10

# ============================================================
# 10. 削除済みファイルをまだ開いているプロセスを確認
# ============================================================
echo ""
echo "=== 10. 削除済みだが開いたままのファイル ==="
lsof | grep "(deleted)" | head -10
echo "(削除済みファイルが存在する場合、ディスクを解放するにはプロセスの再起動が必要)"

# ============================================================
# 11. プロセス名でフィルタ（-c）
# ============================================================
echo ""
echo "=== 11. sshd が開いているファイル ==="
lsof -c sshd | head -10

# ============================================================
# 12. ファイルディスクリプタ数をプロセス別に集計
# ============================================================
echo ""
echo "=== 12. プロセス別オープンファイル数（上位10件） ==="
lsof | awk 'NR>1 {count[$1]++} END {for (p in count) print count[p], p}' \
  | sort -rn | head -10

# ============================================================
# 13. 実用例：ポートが使用中かチェックして起動判断
# ============================================================
echo ""
echo "=== 13. ポート 8080 の使用確認 ==="
PORT=8080
if lsof -i :$PORT > /dev/null 2>&1; then
    echo "⚠️  ポート $PORT は使用中:"
    lsof -i :$PORT
else
    echo "✅ ポート $PORT は空いています"
fi

# ============================================================
# 14. 実用例：特定プロセスのネットワーク接続数を確認
# ============================================================
echo ""
echo "=== 14. sshd の接続数カウント ==="
COUNT=$(lsof -c sshd -i 2>/dev/null | grep -c ESTABLISHED || echo 0)
echo "sshd ESTABLISHED 接続数: $COUNT"

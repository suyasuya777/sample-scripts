#!/bin/bash
# ============================================================
# strace サンプル集 - SRE向け
# ============================================================
# ※ strace は root または CAP_SYS_PTRACE 権限が必要な場合があります
# ※ 本番環境では対象プロセスに負荷がかかるため注意して使用してください

# ============================================================
# 1. 基本：コマンドを strace で実行してシステムコールをトレース
# ============================================================
echo "=== 1. ls コマンドのシステムコールをトレース ==="
strace ls /tmp 2>&1 | head -20

# ============================================================
# 2. システムコールの統計サマリー（-c）
# ============================================================
echo ""
echo "=== 2. ls のシステムコール統計 ==="
strace -c ls /tmp 2>&1

# ============================================================
# 3. 特定のシステムコールのみトレース（-e）
# ============================================================
echo ""
echo "=== 3. open/openat（ファイルオープン）のみトレース ==="
strace -e trace=openat ls /tmp 2>&1 | head -15

echo ""
echo "=== 3b. ネットワーク関連システムコールのみトレース ==="
strace -e trace=network curl -s http://localhost 2>&1 | head -15

# ============================================================
# 4. 出力をファイルに保存（-o）
# ============================================================
echo ""
echo "=== 4. strace 結果をファイルに保存 ==="
strace -o /tmp/strace_output.txt ls /tmp 2>/dev/null
echo "保存先: /tmp/strace_output.txt（行数: $(wc -l < /tmp/strace_output.txt)）"
head -5 /tmp/strace_output.txt

# ============================================================
# 5. タイムスタンプを付与（-t / -tt / -ttt）
# ============================================================
echo ""
echo "=== 5. タイムスタンプ付きでトレース（-tt: マイクロ秒） ==="
strace -tt ls /tmp 2>&1 | head -10

# ============================================================
# 6. 各システムコールの実行時間を表示（-T）
# ============================================================
echo ""
echo "=== 6. 各システムコールの所要時間を表示 ==="
strace -T ls /tmp 2>&1 | head -10

# ============================================================
# 7. 実行中のプロセスにアタッチ（-p）
# ============================================================
echo ""
echo "=== 7. 実行中プロセスへのアタッチ（コマンド例） ==="
cat << 'HELP'
# PID 1234 のプロセスにアタッチして5秒間トレース
strace -p 1234

# アタッチして統計サマリーを取得（Ctrl+C で終了）
strace -p 1234 -c

# アタッチして結果をファイルに保存
strace -p 1234 -o /tmp/strace_pid1234.txt

# 子プロセスも含めてトレース（-f）
strace -p 1234 -f
HELP

# ============================================================
# 8. 子プロセスもトレース（-f）
# ============================================================
echo ""
echo "=== 8. 子プロセスも含めてトレース ==="
strace -f bash -c "ls /tmp && echo done" 2>&1 | head -15

# ============================================================
# 9. 文字列の長さ制限を変更（-s）
# ============================================================
echo ""
echo "=== 9. 文字列を256文字まで表示（デフォルトは32文字） ==="
strace -s 256 cat /etc/hostname 2>&1 | head -10

# ============================================================
# 10. ファイルアクセス系のみトレース（障害調査定番）
# ============================================================
echo ""
echo "=== 10. ファイルアクセス系システムコールのみ ==="
strace -e trace=file ls /tmp 2>&1 | head -15

# ============================================================
# 11. プロセス系のみトレース
# ============================================================
echo ""
echo "=== 11. プロセス生成系システムコールのみ ==="
strace -e trace=process bash -c "echo hello" 2>&1 | head -10

# ============================================================
# 12. 実用例：アプリが読み込む設定ファイルを特定
# ============================================================
echo ""
echo "=== 12. curl が読み込むファイルを特定 ==="
strace -e trace=openat curl -s http://localhost 2>&1 \
  | grep -v "No such file" | head -15

# ============================================================
# 13. 実用例：遅いシステムコールを特定（-T + sort）
# ============================================================
echo ""
echo "=== 13. 実行時間の長いシステムコールを特定 ==="
strace -T ls /tmp 2>&1 \
  | grep -oP '<[0-9.]+>' \
  | tr -d '<>' \
  | awk '{sum+=$1; count++} END {printf "平均所要時間: %.6f 秒 / %d calls\n", sum/count, count}'

# ============================================================
# 14. 実用例：エラーになったシステムコールのみ表示（-e trace=!成功）
# ============================================================
echo ""
echo "=== 14. エラーになったシステムコールのみ表示 ==="
strace ls /nonexistent 2>&1 | grep "= -1"

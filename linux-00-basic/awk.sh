#!/bin/bash
# ============================================================
# awk サンプル集 - SRE向け
# ============================================================

# ── 前提：サンプルファイルを作成 ─────────────────────────
cat > /tmp/app.log << 'EOF'
2024-01-15 10:00:01 INFO  web  GET    /api/users       200 12
2024-01-15 10:00:02 ERROR db   -      -                -   -
2024-01-15 10:00:03 WARN  web  GET    /api/items       200 1523
2024-01-15 10:00:04 INFO  web  POST   /api/users       201 45
2024-01-15 10:00:05 ERROR web  GET    /api/orders      500 -
2024-01-15 10:00:06 INFO  web  GET    /api/users       200 8
2024-01-15 10:00:07 INFO  web  DELETE /api/users/5     204 20
2024-01-15 10:00:08 WARN  web  GET    /api/search      200 980
2024-01-15 10:00:09 INFO  web  GET    /api/items       200 55
2024-01-15 10:00:10 ERROR web  POST   /api/payments    503 -
EOF

cat > /tmp/metrics.tsv << 'EOF'
node01	cpu	85.2
node01	mem	72.1
node01	disk	45.0
node02	cpu	12.3
node02	mem	88.5
node02	disk	91.2
node03	cpu	55.7
node03	mem	60.0
node03	disk	30.5
EOF

echo "=== サンプルログ ==="
cat /tmp/app.log
echo ""

# ============================================================
# 1. 特定フィールドを抽出（$N で N番目のフィールド）
# ============================================================
echo "=== 1. 日時・ログレベル・パスを抽出（$1 $2 $3 $6） ==="
awk '{print $1, $2, $3, $6}' /tmp/app.log

# ============================================================
# 2. 条件フィルタ：ERRORの行だけ表示
# ============================================================
echo ""
echo "=== 2. ERRORの行のみ表示 ==="
awk '$3 == "ERROR"' /tmp/app.log

# ============================================================
# 3. 数値比較：レスポンスタイム 500ms 超の行
# ============================================================
echo ""
echo "=== 3. レスポンスタイム 500ms 超（$8 > 500） ==="
awk '$8 > 500 {print $0}' /tmp/app.log

# ============================================================
# 4. 集計：ログレベルごとの件数
# ============================================================
echo ""
echo "=== 4. ログレベルごとの件数 ==="
awk '{count[$3]++} END {for (k in count) print k, count[k]}' /tmp/app.log

# ============================================================
# 5. 集計：ステータスコードごとの件数
# ============================================================
echo ""
echo "=== 5. HTTPステータスコードごとの件数 ==="
awk '$7 ~ /^[0-9]+$/ {count[$7]++} END {for (k in count) print k, count[k]}' /tmp/app.log \
  | sort

# ============================================================
# 6. 平均値の計算：レスポンスタイムの平均
# ============================================================
echo ""
echo "=== 6. レスポンスタイムの平均（ms） ==="
awk '$8 ~ /^[0-9]+$/ {sum += $8; cnt++} END {printf "平均: %.1f ms\n", sum/cnt}' /tmp/app.log

# ============================================================
# 7. 最大・最小
# ============================================================
echo ""
echo "=== 7. レスポンスタイムの最大・最小 ==="
awk '$8 ~ /^[0-9]+$/ {
    if (NR==1 || $8 > max) { max=$8; max_line=$0 }
    if (NR==1 || $8 < min) { min=$8; min_line=$0 }
  }
  END {
    print "最大:", max, "ms →", max_line
    print "最小:", min, "ms →", min_line
  }' /tmp/app.log

# ============================================================
# 8. フィールド区切り変更（-F）: TSVファイル処理
# ============================================================
echo ""
echo "=== 8. TSVファイル：CPU使用率が80%超のノード ==="
awk -F'\t' '$2 == "cpu" && $3 > 80 {print $1, "CPU:", $3"%"}' /tmp/metrics.tsv

# ============================================================
# 9. NR（行番号）/ NF（フィールド数）の利用
# ============================================================
echo ""
echo "=== 9. ヘッダーをスキップして2行目以降を処理 ==="
# NR > 1 でヘッダースキップ（ここでは2行目以降のERRORを表示）
awk 'NR > 2 && $3 == "ERROR"' /tmp/app.log

echo ""
echo "=== 9b. 最終行のみ表示（END ブロック） ==="
awk 'END {print "最終行:", $0}' /tmp/app.log

# ============================================================
# 10. BEGIN / END ブロック：ヘッダー・フッターを追加
# ============================================================
echo ""
echo "=== 10. BEGIN/END でヘッダー・サマリーを追加 ==="
awk '
  BEGIN {
    print "---- ログ解析レポート ----"
    print "日時\t\t\tレベル\tパス\t\t\tステータス\t応答時間"
  }
  $3 == "INFO" || $3 == "WARN" || $3 == "ERROR" {
    printf "%s %s\t%s\t%-20s\t%s\t%sms\n", $1, $2, $3, $6, $7, $8
    total++
    if ($3 == "ERROR") err++
  }
  END {
    print "---- サマリー ----"
    print "総リクエスト数:", total
    print "エラー数:", err+0
  }
' /tmp/app.log

# ============================================================
# 11. 実用例：ノードごとのリソース使用率サマリー
# ============================================================
echo ""
echo "=== 11. ノードごとのメトリクスサマリー ==="
awk -F'\t' '
  {data[$1 SUBSEP $2] = $3; nodes[$1]=1}
  END {
    print "NODE\t\tCPU\tMEM\tDISK"
    for (node in nodes) {
      printf "%s\t\t%s%%\t%s%%\t%s%%\n", node, data[node SUBSEP "cpu"], data[node SUBSEP "mem"], data[node SUBSEP "disk"]
    }
  }
' /tmp/metrics.tsv

# ============================================================
# 12. 実用例：エラー率の計算
# ============================================================
echo ""
echo "=== 12. エラー率の計算 ==="
awk '
  {total++}
  $3 == "ERROR" {err++}
  END {
    printf "総行数: %d, エラー数: %d, エラー率: %.1f%%\n", total, err, (err/total)*100
  }
' /tmp/app.log

#!/bin/bash
# ============================================================
# dig / nslookup サンプル集 - SRE向け
# ============================================================

DOMAIN="example.com"

# ============================================================
# dig
# ============================================================

# ============================================================
# 1. dig：基本的なAレコード検索
# ============================================================
echo "=== 1. dig：Aレコード（IPアドレス）を検索 ==="
dig example.com A

# ============================================================
# 2. dig：簡潔な出力（+short）
# ============================================================
echo ""
echo "=== 2. IPアドレスのみ表示（+short） ==="
dig example.com A +short

# ============================================================
# 3. dig：各レコードタイプの検索
# ============================================================
echo ""
echo "=== 3. 各レコードタイプを検索 ==="
echo "--- MXレコード（メールサーバー） ---"
dig example.com MX +short

echo "--- NSレコード（ネームサーバー） ---"
dig example.com NS +short

echo "--- TXTレコード（SPF/DKIM等） ---"
dig example.com TXT +short

echo "--- CNAMEレコード ---"
dig www.example.com CNAME +short

# ============================================================
# 4. dig：逆引き（IPアドレス → ホスト名）
# ============================================================
echo ""
echo "=== 4. 逆引きDNS（PTRレコード） ==="
dig -x 93.184.216.34 +short

# ============================================================
# 5. dig：特定のDNSサーバーに問い合わせ（@）
# ============================================================
echo ""
echo "=== 5. 特定DNSサーバーに問い合わせ ==="
echo "--- Google DNS (8.8.8.8) に問い合わせ ---"
dig @8.8.8.8 example.com A +short

echo "--- Cloudflare DNS (1.1.1.1) に問い合わせ ---"
dig @1.1.1.1 example.com A +short

# ============================================================
# 6. dig：TTLを確認
# ============================================================
echo ""
echo "=== 6. TTL（キャッシュ有効期間）を確認 ==="
dig example.com A | awk '/^example\.com/ {print "TTL:", $2, "秒"}'

# ============================================================
# 7. dig：DNSSECの確認（+dnssec）
# ============================================================
echo ""
echo "=== 7. DNSSEC情報を含めて検索 ==="
dig example.com A +dnssec | grep -E "RRSIG|DNSKEY|flags" | head -5

# ============================================================
# 8. dig：応答時間の計測（+stats）
# ============================================================
echo ""
echo "=== 8. 応答時間の確認 ==="
dig example.com A +stats | grep "Query time"

# ============================================================
# 9. dig：SOAレコード（ゾーン情報）
# ============================================================
echo ""
echo "=== 9. SOAレコード（ゾーン情報） ==="
dig example.com SOA +short

# ============================================================
# 10. dig：トレースモード（名前解決の経路を確認）
# ============================================================
echo ""
echo "=== 10. 名前解決の経路をトレース ==="
dig example.com +trace 2>&1 | head -25

# ============================================================
# nslookup
# ============================================================

# ============================================================
# 11. nslookup：基本的なIPアドレス検索
# ============================================================
echo ""
echo "=== 11. nslookup：基本検索 ==="
nslookup example.com

# ============================================================
# 12. nslookup：特定DNSサーバーに問い合わせ
# ============================================================
echo ""
echo "=== 12. nslookup：特定DNSサーバーに問い合わせ ==="
nslookup example.com 8.8.8.8

# ============================================================
# 13. nslookup：レコードタイプを指定
# ============================================================
echo ""
echo "=== 13. nslookup：MXレコード検索 ==="
nslookup -type=MX example.com

# ============================================================
# 14. nslookup：逆引き
# ============================================================
echo ""
echo "=== 14. nslookup：逆引き ==="
nslookup 93.184.216.34

# ============================================================
# 15. 実用例：複数ドメインのDNS解決時間を計測
# ============================================================
echo ""
echo "=== 15. 複数ドメインのDNS解決時間を比較 ==="
DOMAINS=("example.com" "google.com" "github.com")
for domain in "${DOMAINS[@]}"; do
    TIME=$(dig "$domain" A +stats 2>/dev/null | grep "Query time" | awk '{print $4}')
    IP=$(dig "$domain" A +short | head -1)
    printf "%-20s IP: %-15s 応答時間: %s ms\n" "$domain" "$IP" "$TIME"
done

# ============================================================
# 16. 実用例：内部DNS と 外部DNS の解決結果を比較
# ============================================================
echo ""
echo "=== 16. DNSサーバー間の解決結果を比較 ==="
DOMAIN="example.com"
echo "ドメイン: $DOMAIN"
printf "Google DNS  (8.8.8.8): %s\n" "$(dig @8.8.8.8 $DOMAIN A +short | head -1)"
printf "Cloudflare  (1.1.1.1): %s\n" "$(dig @1.1.1.1 $DOMAIN A +short | head -1)"
printf "システムDNS          : %s\n" "$(dig $DOMAIN A +short | head -1)"

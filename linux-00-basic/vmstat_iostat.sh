#!/bin/bash
# ============================================================
# vmstat / iostat サンプル集 - SRE向け
# ============================================================

# ============================================================
# vmstat：仮想メモリ・CPU・I/O統計
# ============================================================

# ============================================================
# 1. vmstat：現在のシステム統計を表示
# ============================================================
echo "=== 1. vmstat：現在のシステム統計 ==="
vmstat
echo ""
echo "【列の見方】"
cat << 'HELP'
procs:  r=実行待ちプロセス数  b=I/O待ちプロセス数
memory: swpd=スワップ使用量(KB)  free=空きメモリ  buff=バッファ  cache=キャッシュ
swap:   si=スワップイン(KB/s)  so=スワップアウト(KB/s)
io:     bi=ブロック読み込み(blocks/s)  bo=ブロック書き込み(blocks/s)
system: in=割り込み/秒  cs=コンテキストスイッチ/秒
cpu:    us=ユーザ  sy=システム  id=アイドル  wa=I/O待機  st=steal
HELP

# ============================================================
# 2. vmstat：2秒間隔で5回表示
# ============================================================
echo ""
echo "=== 2. 2秒間隔で5回表示 ==="
vmstat 2 5

# ============================================================
# 3. vmstat：メモリ情報をMB単位で表示（-S M）
# ============================================================
echo ""
echo "=== 3. MB単位で表示 ==="
vmstat -S M 1 3

# ============================================================
# 4. vmstat：ディスク統計（-d）
# ============================================================
echo ""
echo "=== 4. ディスク統計 ==="
vmstat -d | head -10

# ============================================================
# 5. vmstat：スラブキャッシュ統計（-m）
# ============================================================
echo ""
echo "=== 5. スラブキャッシュ統計（上位10件） ==="
vmstat -m | head -10

# ============================================================
# 6. vmstat：タイムスタンプ付きで出力（-t）
# ============================================================
echo ""
echo "=== 6. タイムスタンプ付きで3回出力 ==="
vmstat -t 1 3

# ============================================================
# 7. vmstat：スワップ使用を監視してアラート
# ============================================================
echo ""
echo "=== 7. スワップ使用量を確認 ==="
SWPD=$(vmstat | awk 'NR==3 {print $3}')
if [ "$SWPD" -gt 0 ]; then
    echo "⚠️  スワップ使用中: ${SWPD}KB"
else
    echo "✅ スワップ未使用"
fi

# ============================================================
# iostat：CPU・ディスクI/O統計
# ============================================================

# ============================================================
# 8. iostat：基本的なCPUとI/O統計
# ============================================================
echo ""
echo "=== 8. iostat：CPU・I/O統計 ==="
iostat

# ============================================================
# 9. iostat：デバイス別の詳細統計（-x）
# ============================================================
echo ""
echo "=== 9. デバイス別詳細統計 ==="
iostat -x 1 2
echo ""
echo "【主要列の見方】"
cat << 'HELP'
r/s    : 読み込みリクエスト数/秒
w/s    : 書き込みリクエスト数/秒
rkB/s  : 読み込みKB/秒
wkB/s  : 書き込みKB/秒
await  : I/Oリクエストの平均待機時間(ms) ← 重要指標
%util  : ディスクが使用されている時間の割合 ← 重要指標（100%に近いと飽和）
HELP

# ============================================================
# 10. iostat：MB単位で表示（-m）
# ============================================================
echo ""
echo "=== 10. MB単位で表示 ==="
iostat -m 1 2

# ============================================================
# 11. iostat：CPUのみ表示（-c）
# ============================================================
echo ""
echo "=== 11. CPU統計のみ表示 ==="
iostat -c 1 3

# ============================================================
# 12. iostat：特定デバイスのみ表示
# ============================================================
echo ""
echo "=== 12. 特定デバイスのみ（例: sda または vda） ==="
DISK=$(lsblk -d -o NAME | grep -E "^(sd|vd|nvme)" | head -1)
if [ -n "$DISK" ]; then
    iostat -x "$DISK" 1 2
else
    echo "(ブロックデバイスが見つかりません)"
fi

# ============================================================
# 13. 実用例：I/O待機率が高い場合の検出
# ============================================================
echo ""
echo "=== 13. I/O待機率（wa）の監視 ==="
WA=$(vmstat 1 2 | awk 'NR==4 {print $16}')
THRESHOLD=20
if [ "$WA" -gt "$THRESHOLD" ]; then
    echo "⚠️  I/O待機率が高い: wa=${WA}%（閾値: ${THRESHOLD}%）"
else
    echo "✅ I/O待機率は正常: wa=${WA}%"
fi

# ============================================================
# 14. 実用例：vmstat + iostat を組み合わせたリソースサマリー
# ============================================================
echo ""
echo "=== 14. システムリソースサマリー ==="
echo "---- CPU ----"
vmstat 1 2 | awk 'NR==4 {printf "us=%s%% sy=%s%% id=%s%% wa=%s%%\n", $13, $14, $15, $16}'

echo "---- Memory ----"
vmstat -S M | awk 'NR==3 {printf "free=%sMB swpd=%sMB cache=%sMB\n", $4, $3, $6}'

echo "---- Disk I/O ----"
iostat -x 1 1 | awk '/^[sv]d|^nvme/ {printf "%s: r/s=%.1f w/s=%.1f await=%.1f %%util=%.1f\n", $1, $4, $5, $10, $NF}'
